package com.review;

public class DenePattern {

	public static void main(String[] args) {
		
		
		for(int i=1; i<=7; i++) {
			for(int k=1; k<=i;k++) {
				System.out.print(k+" ");
			}
			System.out.println(" ");
		}
		
		
		for(int x =7; x>=1; x--) {
			for(int z=1; z<=x; z++) {
				System.out.print(z+" ");
			}
			System.out.println(" ");
		}
		
		
	

	}

}
